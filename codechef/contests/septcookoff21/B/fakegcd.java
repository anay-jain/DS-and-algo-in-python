import java.util.Scanner;

public class fakegcd{

    public static void main(String[] args){
            Scanner scn = new Scanner(System.in);
            int t = scn.nextInt();
            while(t-->0){
                int n = scn.nextInt();
                while(n-->0){
                    System.out.print((n+1)+" ");

                }
                System.out.println(" ");
                
            }
    }
}